const boton1 = document.getElementById('btn-info1');
let desplegado1 = document.getElementById('panel-info1').style.display;

// Escuchamos el clic
boton1.addEventListener('click', () => {
    if ((desplegado1 === "none")|| (desplegado1 === "")){
        document.getElementById("panel-info1").style.display = "block";
        desplegado1 = document.getElementById('panel-info1').style.display;
    }
    else{
        document.getElementById("panel-info1").style.display = "none";
        desplegado1 = document.getElementById('panel-info1').style.display;
    }
    
});
